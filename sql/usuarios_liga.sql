    ID_USUARIO   NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    USERNAME     VARCHAR2(50) UNIQUE NOT NULL,
    PASSWORD     VARCHAR2(200) NOT NULL,
    ROL          VARCHAR2(20)  NOT NULL
);

